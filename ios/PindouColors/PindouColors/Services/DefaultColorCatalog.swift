struct DefaultColorSeedService {
    @MainActor
    func insertMissingDefaults(into context: ModelContext, existingColors: [BeadColor]) throws -> ImportResult {
        let existingKeys = Set(existingColors.map(\.normalizedKey))
        let now = Date()
        var inserted = 0

        for item in DefaultColorCatalog.colors where !existingKeys.contains(item.normalizedKey) {
            context.insert(
                BeadColor(
                    code: item.code,
                    series: item.series,
                    hex: item.hex,
                    displayName: item.displayName,
                    alias: item.alias,
                    sortOrder: item.sortOrder,
                    enabled: item.enabled,
                    sourceURL: item.sourceURL,
                    sourceKey: item.sourceKey,
                    note: item.note,
                    stockCount: item.stockCount,
                    createdAt: now,
                    updatedAt: now,
                    lastSyncedAt: now
                )
            )
            inserted += 1
        }

        try context.save()
        return ImportResult(
            parsed: DefaultColorCatalog.colors.count,
            inserted: inserted,
            updated: 0,
            skipped: DefaultColorCatalog.colors.count - inserted
        )
    }
}
